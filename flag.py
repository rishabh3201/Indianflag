t.fd(800)
t.rt(90)
t.fd(200)
t.rt(90)
t.fd(400)
t.end_fill()

t.penup()
t.lt(90)
t.fd(200)
t.rt(90)
t.pendown()

t.color('orange')
t.begin_fill()
