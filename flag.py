import turtle as t
t.pencolor('blue')
for i in range (24):
	t.fd(100)
	t.backward(100)
	t.rt(15)
t.rt(90)
t.fd(100)
t.lt(90)
t.circle(100)

t.color('green')
t.begin_fill()
t.fd(400)
t.rt(90)
